
// MATH OBJECT 

// Example: Using arithmetic operators for basic arithmetic operations
let x = 10;
let y = 5;

let sum = x + y; // Addition
let difference = x - y; // Subtraction
let product = x * y; // Multiplication
let quotient = x / y; // Division

console.log(sum); // Output: 15
console.log(difference); // Output: 5
console.log(product); // Output: 50
console.log(quotient); // Output: 2

// Example: Accessing mathematical constants
console.log(Math.PI); // Output: 3.141592653589793
console.log(Math.E); // Output: 2.718281828459045

// Example: Using Math methods for complex calculations
let number = 3.7;

let roundedDown = Math.floor(number); // Round down to the nearest integer
let roundedUp = Math.ceil(number); // Round up to the nearest integer
let absoluteValue = Math.abs(-5); // Absolute value
let randomValue = Math.random(); // Generate a random number between 0 and 1

console.log(roundedDown); // Output: 3
console.log(roundedUp); // Output: 4
console.log(absoluteValue); // Output: 5
console.log(randomValue); // Output: (random number between 0 and 1)